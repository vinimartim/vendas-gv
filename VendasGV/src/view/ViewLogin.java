    private void jbLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbLoginMouseClicked
        modelUsuarios.setUsuarioEmail(jtfEmail.getText());
        modelUsuarios.setUsuarioSenha(String.valueOf(jtfSenha.getPassword()));
        
        if (controllerUsuarios.getValidarUsuarioController(modelUsuarios)) {
            new ViewPrincipal().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "E-mail ou senha inv√°lidos");
            jtfEmail.requestFocus();
        }
    }//GEN-LAST:event_jbLoginMouseClicked

